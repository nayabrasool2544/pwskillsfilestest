public class Datatypes
{
	public static void main(String[] args) {
		int number = 10;
		double bignum = 20.30;
		String name = "SYED";
		char ch= 'R';
		boolean value = false;
		System.out.println( "int datatype is " + number);
		System.out.println( "double datatype is  " + bignum);
		System.out.println("String datatype is " + name);
		System.out.println("char datatype is " + ch);
		System.out.println( "boolean datatype type is " + value);
	}
}